import React, { useState, useEffect } from 'react';

const url = 'https://api.github.com/users';

const UseEffectFetchData = () => {
  const [users, setUsers] = useState([]);

  const getUsers = async () => {
    const response = await fetch(url);
    const users = await response.json();
    setUsers(users);
  };

  useEffect(() => {
    getUsers();
  }, []);
  // EMPTY DEPENDENCY ARRAY MEANS THAT USEEFFECT ONLY RUNS ONCE - THIS AVOIDS AN INFINITE LOOP
  // IF YOU RETRIGGER USESTATE (users and setUsers(users) in this case), USE THE DEPENDENCY ARRAY TO AVOID AN INFINITE LOOP

  return (
    <>
      <h3>GitHub Users</h3>
      <ul className='users'>
        {users.map((user) => {
          const { id, login, avatar_url, html_url } = user;
          return (
            <li key={id}>
              <img src={avatar_url} alt={login} />
              <div>
                <h4>{login}</h4>
                <a href={html_url}>Profile</a>
              </div>
            </li>
          );
        })}
      </ul>
    </>
  );
};

export default UseEffectFetchData;
